					<h3>2.1.1.2 Pillar 2</h3>

					<p>The Firm has adopted the “Structured” approach to the calculation of its Pillar 2 Minimum Capital Requirement as outlined in the Committee of European Banking Supervisors Paper, 27 March 2006 which takes the higher of Pillar 1 and 2 as the Internal Capital Adequacy Assessment Process (“ICAAP”) capital requirement.  It has assessed Business Risks by modelling the effect on its capital planning forecasts and assessed Operational Risk by considering if Pillar 2 capital is required taking into account the adequacy of its mitigation.</p>

					<p>Since the Firm’s ICAAP has not identified capital to be held over and above the Pillar 1 requirement, the capital resources detailed above are considered adequate to continue to finance the Firm over the next year.</p>

					<h3>3.0 Risk Management</h3>

					<p>The Firm has established a Risk Management function (“RMF”), headed by the Firm’s Head of Risk to ensure that it has effective systems and controls in place to identify, monitor and manage risks arising in the business. The Firm’s Head of Risk oversees the performance of the Risk Management Function and reports to the Board on whether the Company remains within its risk appetite.</p>

					<p>As risks are identified within the business, appropriate controls are put in place to mitigate these and compliance with them is monitored on a regular basis. The frequency of monitoring in respect of each risk area is determined by the significance of the risk. The Firm does not intend to take any risks with its own capital and ensures that risk taken within the portfolios that it provides advice to is closely monitored. The results of the compliance monitoring performed is reported to the Board by the Compliance Officer.</p>

					<h3>3.1 Operational Risk</h3>

					<p>The Firm places strong reliance on the operational procedures and controls that it has in place to mitigate risk and seeks to ensure that all personnel are aware of their responsibilities in this respect.</p>

					<p>The Firm has identified a number of key operational risks. These relate to disruption of the office facilities, system failures, trade failures and failure of third party service providers. Appropriate policies are in place to mitigate against risks, including appropriate insurance policies and business continuity plans.</p>

					<h3>3.2 Credit Risk</h3>

					<p>The main credit risk to which the Firm is exposed is in respect to the failure of its debtors to meet their contractual obligations. Most the Firm’s receivables are related to investment management and advisory activities. The Firm believes its credit risk exposure is limited since the Firm’s revenue is ultimately related to management fees received from funds. These management fees are drawn throughout the year. Other credit exposures include bank deposits and office rental deposits.</p>

					<p>The Firm undertakes periodic impairment reviews of its receivables. All amounts due to the Firm are current and none have been overdue during the year. As such, due to the low risk of non-payment from its counterparties, management is of the opinion that no provision is necessary. A financial asset is overdue when the counterparty has failed to make a payment when contractually due. Impairment is defined as a reduction in the recoverable amount of a fixed asset or goodwill below its carrying amount.</p>

					<p>GENPRU 2.1.46 states that “When a collective portfolio management investment firm calculates the credit risk capital requirement and the market risk capital requirement for the purpose of calculating the variable capital requirement under GENPRU 2.1.40 R it must do so only in respect of designated investment business. For this purpose managing an AIF or managing a UCITS is excluded from designated investment business.” Pollen Street Capital does not undertake any designated investment activity other than managing an AIF and so is not required to hold credit risk capital.</p>

					<h3>3.3 Market Risk</h3>

					<p>Since the Firm holds no trading book positions on its own account, and its primary bank account is in GBP and the majority of its fee income is converted to GBP at the first opportunity, the Firm’s exposure to foreign currency risk is not significant. Since the settlement of debtor balances take place without undue delay, the timing of the amount becoming payable and subsequently being paid is such that it is not considered to present a material risk to the Firm. The Firm is therefore of the opinion that market risk is not a material risk to the Firm.</p>

					<p>GENPRU 2.1.46 states that “When a collective portfolio management investment firm calculates the credit risk capital requirement and the market risk capital requirement for the purpose of calculating the variable capital requirement under GENPRU 2.1.40 R it must do so only in respect of designated investment business. For this purpose managing an AIF or managing a UCITS is excluded from designated investment business.” Pollen Street Capital does not undertake any designated investment activity other than managing an AIF and so is not required to hold credit risk and market risk capital.</p>

					<h3>3.4 Liquidity Risk</h3>

					<p>Liquidity risk is the risk that the Firm does not have adequate liquid assets to meet its obligations as they fall due.  As the firm does not have material illiquid assets, this risk is not considered to be material.</p>

					<h3>3.5 Securitisation Risk</h3>

					<p>Securitisation risk is the risk that the capital resources held by the firm in respect of assets it has securitised are insufficient.  The firm is not exposed to any securitisation vehicles and is therefore not exposed to securitisation risk.</p>

					<h3>3.6 Insurance Risk</h3>

					<p>Insurance risk is the risk that insurance liabilities occur at a different time or in a different quantum to that expected.  The firm does not undertake insurance activities and is therefore not exposed to insurance risk.</p>

					<h3>3.7 Pension Obligation Risk</h3>

					<p>Pension obligation risk is the risk that the firm is required to make contractual or non-contractual contributions to a pension scheme.  The firm does not operate a defined benefit pension scheme and does not have any pension obligations.  The firm is therefore not exposed to pension obligation risk.</p>

					<h3>3.8 Concentration Risk</h3>

					<p>Concentration risk is the risk arising from the firm having a large exposure to a particular counterparty, market or collateral type.  The firm does not undertake lending activities as principal and is therefore not exposed to concentration risk.</p>

					<h3>3.9 Residual Risk</h3>

					<p>Residual risk is the risk that the firm’s credit mitigation techniques are ineffective.  The firm does not undertake credit activities and hence does not have any credit mitigation techniques.  The firm is therefore not exposed to residual risk.</p>

					<h3>3.10 Business Risk</h3>

					<p>Business risk is the risk that the firm is may not be able to carry out its business plan or strategy.  In analysing the impact of this risk, the Firm has considered a variety of stress tests covering different scenarios and has concluded the capital held under pillar 1 adequately covers these risks.</p>

					<h3>3.11 Interest Rate Risk</h3>

					<p>Interest rate risk is the risk that the firm’s income or expenses vary as a result of prevailing interest rates.  The firm does not have any borrowings and earns an immaterial amount of interest income from its treasury balances. However, the firm derives a portion of its performance fees from AIFs whose income, and hence the Firm’s fees, may depend on interest. The firm considers this to be part of the overall business risk of the firm and so has captured this as business risk. The firm is therefore not materially exposed to further interest rate risk.</p>

					<h3>3.12 Other Risks</h3>

					<p>No other risks have been identified.</p>

					<h3>4.0 Remuneration Code</h3>

					<p>The Firm has adopted a remuneration policy and procedures that comply with the requirements for remuneration management in accordance with the FCA AIFM remuneration code (SYSC 19B) of the FCA’s Senior Management Arrangements, Systems and Controls Sourcebook (“SYSC”), and in accordance with ESMA’s Guidelines on sound remuneration policies. The Firm has considered all the proportionality elements in line with the FCA Guidance and, pursuant to this application, has disapplied certain provisions of the code where relevant.</p>

					<p>The remuneration policy is reviewed by the Firm’s Remuneration Committee.</p>
