					<h3>1.0 Introduction</h3>

					<p>Pollen Street Capital Limited (“Pollen Street” or the “Firm”) is required by the FCA to disclose information relating to the capital it holds and each material category of risk it faces to assist users of its accounts and to encourage market discipline.</p>

					<p>The Capital Requirements Directive (“CRD”) created a revised regulatory capital framework across Europe covering how much capital financial services firms must retain. In the United Kingdom, rules and guidance are provided in the General Prudential Sourcebook (“GENPRU”) for Banks, Building Societies and Investments Firms (“BIPRU”).</p>

					<p>The FCA framework consists of three “Pillars”.</p>

					<ul>
						<li>Pillar 1 sets out the minimum capital requirements that companies need to retain to meet their credit, market and operational risk;</li>

						<li>Pillar 2 requires companies to assess whether their Pillar 1 capital is adequate to meet their risks and is subject to annual review by the FCA;</li>

						<li>Pillar 3 requires companies to develop a set of disclosures which will allow market participants to assess key information about its underlying risks, risk management controls and capital position. These disclosures are complimentary to Pillar 1 and Pillar 2.</li>
					</ul>

					<p>Rule 11 of BIPRU sets out the provisions for Pillar 3 disclosure. The rules provide that companies may omit one or more of the required disclosures if such omission is regarded as immaterial. Information is considered material if its omission or misstatement could change or influence the decision of a user relying on the information. In addition, companies may also omit one or more of the required disclosures where such information is regarded as proprietary or confidential. The Firm believes that the disclosure of this document meets its obligation with respect to Pillar 3. Pollen Street will make the Pillar 3 quantitative disclosure on an annual basis, following the completion of its ICAAP.</p>

					<p>In addition, the Firm, on account of its classification as a full scope Alternative Investment Fund Manager (“AIFM”), is subject to a parallel "own funds" requirement as detailed in Article 9 of the Alternative Investment Fund Managers Directive.</p>

					<h3>2.0 Firm Overview</h3>

					<p>Pollen Street is incorporated in the UK and is authorised and regulated by the FCA as a a collective portfolio management investment firm and full scope AIFM. As such PSC calculates its capital requirement based upon three methodologies, each of which is discussed further below:</p>

					<ul>
						<li>The higher of the base capital requirement and the capital requirements under GENPRU 2.1.45;</li>
						<li>The requirements of the Pillar 2 Rule; and...</li>
						<li>The requirements of AIFMD.</li>
					</ul>

					<p>The CEO is responsible for the day to day management of the Group.</p>

					<p>Risk within the Group is managed by use of the following:</p>

					<ul>
						<li>Senior management is accountable for designing, implementing and monitoring the process of risk management and implementing  it into the day-to-day business activities of the Firm;</li>

						<li>The Board on behalf of the Firm has approved a risk appetite statement;</li>

						<li>The Firm identifies the most material risks to its business and subjects them to scenario analyses and stress tests in order to assist in its risk management and capital planning;</li>

						<li>The Group has implemented an operational risk event data collection procedure to ensure that all events, be they actual operational losses or near misses, are captured.</li>
					</ul>

