					<h1>The Pollen Street Capital ESG policy</h1>
					<p>
					<p><strong>I. Purpose</strong><br /> 
					This document sets out Pollen Street Capital’s (“PSC’s”) approach to the management of environmental, social and governance (ESG) issues.</p>


					<p><strong>II. Scope</strong><br />
					The ESG Policy covers all PSC funds (PE and Credit), existing portfolio companies as well as new acquisitions, all investment activities and PSC’s own operations. All PSC staff are required to adhere to the ESG Policy.</p>


					<p><strong>III. Our Guiding Principles</strong><br />
					PSC recognises that the industry in which it operates can have a significant impact on society and the environment. The sector reaches a large number of people across many countries and involves the use of a significant amount of the earth’s natural resources.</p>

					<p>PSC believes that ESG issues should be integral to the investment process and has formalised its approach to managing these issues. PSC understands that the effective ownership and management of a company or an investment creates benefits for all stakeholders, for example employees, customers, suppliers and shareholders. Implementing best practice management techniques for ESG issues contributes to both mitigating risks and capturing opportunities that enhance the long-term value of the PSC funds’. PSC is committed to helping its investment funds make responsible investments in order to create sustainable, long-term value in close partnership with management teams.</p>

					<p>[PSC is a signatory of the Principles for Responsible Investment (PRI). It is committed to implementing its Principles with a view to aligning its investment management and advisory activities to the interests of wider stakeholders.]</p>
				</div>
