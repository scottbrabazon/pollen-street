					<p><strong>IV. Our commitments to invest responsibly</strong><br />
					PSC commits to</p>
					<ul>
						<li><strong>Comprehensive and robust ESG governance.</strong><span> </span>The CEO and the Group Risk Manager are responsible for overseeing PSC’s ESG management activities. The Deal and Operations teams are responsible for implementing the ESG Policy and Procedures, with the support of General Counsel. PSC provides its staff with specific ESG training and resources to help them to fulfil their ESG commitments.</li>

						<li><strong>Ensuring it complies with all ESG-related legislation.</strong><span> </span>PSC has set its own ESG Guidelines and strongly encourages all portfolio companies in the PSC funds to meet them. Where appropriate, PSC expects the portfolio companies to comply with additional voluntary standards, [such as the IFC Performance Standards,] particularly where local legislative standards are weak or laws are poorly enforced. It supports the payment of competitive wages and benefits to employees; providing a safe and healthy workplace in conformance with national and local law.</li>

						<li><strong>No tolerance of irresponsible or corrupt business practices.</strong><span> </span>PSC adopt strict policies that prohibit bribery and other improper payments to public officials consistent with [the U.S. Foreign Corrupt Practices Act, similar laws in other countries, and the OECD Anti-Bribery Convention].</li>
						<li><strong>Embedding ESG issue management throughout the deal cycle:</strong>
						<ul>
							<li><strong>Pre-acquisition:</strong>
								PSC assesses ESG risks and management standards when evaluating all investment opportunities. PSC analyses inherent ESG risks and relevant management activities (to the extent that information is available) throughout the PSC investment process. When material ESG issues are identified, they may be included in formal submissions to the Investment Committee, and external advisors may be engaged to carry out additional ESG-related due diligence as needed. Where management of, or performance on, a material issue is considered by PSC to need improvement a corrective action plan will be completed.<br />If PSC concludes that the ESG risks of a target company are too great and cannot be appropriately mitigated, no investment is made.
							</li>
						</ul>
					</ul>
				</div>
