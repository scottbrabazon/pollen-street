					<p><strong>V. How we Operate</strong><br /></p>
					<p>PSC seeks to embed effective ESG practices in its own operations as well as in its investment<br />activities. It encourages it’s portfolio companies to adopt these same principles in a way that is consistent with their fiduciary duties and therefore, PSC commits to:</p>
					<ul>
						<li><strong>Supporting honest and ethical behaviours in its activities</strong>. All PSC employees are subject to, and receive training on PSC policies, including PSC’s Code of Ethics. All employees are required to acknowledge they have received, read and understood and agree to comply with PSC policies upon joining and on a quarterly basis.</li>
						<li><strong>Supporting appropriately selected charities</strong>. PSC supports charities that focus on helping [young people and disenfranchised members of society integrate themselves into society]. It provides direct financial help and encourages its staff to provide “on the ground” support.</li>
						<li><strong>Promoting diversity in the workplace</strong>. PSC is an equal opportunities employer and we respect the diversity of our people. Our employees are focused on driving superior performance through their initiative, good judgment and creativity. We seek to employ, develop and retain a diverse talented group of people who make informed career and life style choices within a supportive working environment.</li>
					</ul>
