					<ul>
						<ul>
							<li><strong>Post-closing:</strong>PSC supports portfolio companies in understanding and improving their ESG performance, where necessary. Where there are material issues identified during the diligence process, these issues are included in a short tem action plan post-close. Within the first 12 months of ownership by the PSC funds PSC conducts a detailed review of each company’s ESG management practices. These reviews represent an opportunity for PSC to engage with the portfolio companies and to offer support to help them meet PSC’s ESG Guidelines. In cases where PSC identifies significant need for value protection or potential for value creation, a further, comprehensive review of the portfolio company’s ESG activities, is undertaken, potentially with the assistance of external experts.</li>
							<li><strong>Monitoring and continuous improvement:</strong>
							PSC monitors portfolio companies’ and investments ESG performance with a view to identify and respond to opportunities for further improvement on an ongoing basis. PSC has a process to allow portfolio companies to promptly report any material ESG incidents and remains ready to provide support to portfolio companies should these situations arise. Where management of, or performance on, a material issue is considered by PSC to need improvement, PSC will work with management to support the development of a corrective action plan.</li>
							<li><strong>Exit:</strong>Where relevant, PSC discloses relevant ESG information, gathered through the preacquisition and PSC fund ownership periods, to potential buyers at the exit stage.</li>
						</ul>
					</ul>
					<ul>
						<li><strong>Engaging with investors and stakeholders on ESG issues</strong>. PSC includes ESG information in periodic reports to investors, makes the ESG Policy available to investors and responds to ad hoc requests for ESG information from investors. The format of this reporting may vary among written public reports, verbal informal reports, or confidential fund or asset-level reports. PSC will seek to actively engage relevant stakeholders in an effort to make informed decisions that may affect these stakeholders.</li>
					</ul>
