					<p><br /><strong>II. Our performance standards ?</strong></p>
					<p>When making an investment decision, the assessment may consider as a guide the International Finance Corporation (IFC) 2012 Performance Standards on Environmental and Social Sustainability (Performance Standards) as they apply to the PSC investment process, as well as any additional ESG guidance that PSC has sought and considers to be applicable. <br />Our performance standard</p>
					<ul>
						<li>Identifies and evaluate environmental and social risks and impacts of the investment.</li>
						<li>assesses what can be done to anticipate and avoid, or where avoidance is not possible, minimize, the impact and, where residual impacts remain, compensate/offset for risks and impacts to workers, the local community, and the environment.</li>
						<li>Promotes improved environmental and social performance of portfolio companies and investment partners through the effective use of management systems.</li>
						<li>ensures that grievances from local communities and external communications from others are recorded and actioned </li>
					</ul>
					<p><strong>III. How do we govern this policy?</strong></p>
					<p>The ESG Policy is updated annually to reflect ongoing efforts to improve its ESG approach, as well as emerging requirements from stakeholders and evolving good practices.</p>
					<p>PSC will actively communicate the ESG Policy to investment teams and will use the ESG Policy as a basis to engage internal and external stakeholders on ESG management, as appropriate.</p>
					<p>The CEO and Group Risk Manager sponsor this ESG Policy and are accountable to the Boards of PSC Holdings Limited for its implementation. The Board receives regular report on ESG matters</p>
					<p>PSC use governance structures that provide appropriate levels of oversight in the areas of audit, risk management, allocation and potential conflicts of interest. It implements compensation and other policies that align the interests of owners and management.</p>
